import mongoose from "mongoose";

// creating a schema for the Post collection
const userSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  id: {
    type: String,
  },
});

export default mongoose.model("User", userSchema);
